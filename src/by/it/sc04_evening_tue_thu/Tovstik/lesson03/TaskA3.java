/*
Измените переменные и строку вывода в программе так,
чтобы она расчитывала и печатала следующее выражение
3*3+4*4=25
 */

class TaskA3 {
    public static void main(String[] args) {
        int i = 3;
        int j = 3;
        int p = 4;
        int x = 4;
        int k = i * j + p * x;
        System.out.println(i + "*" + j + "+" + p + "*" + x + "=" + k);
    }
}
