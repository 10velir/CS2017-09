package by.it.sc02_morning.menchytsky.examples.lesson05;

// Demonstrate the while loop.
class WhileDemo {   
  public static void main(String args[]) { 
    char ch; 
 
    // print the alphabet using a while loop 
    ch = 'a'; 
    while(ch <= 'z') { 
      System.out.print(ch); 
      ch++; 
    } 
  }   
}